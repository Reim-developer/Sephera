use std::{
    fs,
    path::{Path, PathBuf},
};

use anyhow::{Context, Result};

use crate::workspace_root;

use self::{
    datasets::{
        AVAILABLE_DATASET_NAMES, DEFAULT_DATASET_NAMES, resolve_dataset_specs,
    },
    writer::generate_dataset,
};

pub const DEFAULT_BENCHMARK_CORPUS_RELATIVE: &str =
    "benchmarks/generated_corpus";

#[must_use]
pub fn default_benchmark_corpus_path() -> PathBuf {
    workspace_root().join(DEFAULT_BENCHMARK_CORPUS_RELATIVE)
}

#[must_use]
pub const fn default_benchmark_dataset_names() -> &'static [&'static str] {
    DEFAULT_DATASET_NAMES
}

#[must_use]
pub const fn available_benchmark_dataset_names() -> &'static [&'static str] {
    AVAILABLE_DATASET_NAMES
}

/// # Errors
///
/// Returns an error when the output directory cannot be prepared, a requested dataset is unknown,
/// or fixture files cannot be written.
pub fn generate_benchmark_corpus(
    output_root: &Path,
    clean: bool,
    dataset_names: &[&str],
) -> Result<()> {
    if clean && output_root.exists() {
        fs::remove_dir_all(output_root).with_context(|| {
            format!("failed to remove `{}`", output_root.display())
        })?;
    }

    fs::create_dir_all(output_root).with_context(|| {
        format!("failed to create `{}`", output_root.display())
    })?;

    let datasets = resolve_dataset_specs(dataset_names)?;
    for dataset in &datasets {
        generate_dataset(output_root, dataset)?;
    }

    let selected_names = datasets
        .iter()
        .map(|dataset| dataset.name)
        .collect::<Vec<_>>()
        .join(", ");
    fs::write(
        output_root.join("README.txt"),
        format!(
            "Generated by `sephera_tools generate-benchmark-corpus`. Datasets: {selected_names}.\n"
        ),
    )
    .with_context(|| format!("failed to write `{}`", output_root.display()))?;

    Ok(())
}
