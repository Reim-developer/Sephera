use std::{
    fs,
    path::{Path, PathBuf},
};

use anyhow::{Context, Result};

use crate::workspace_root;

use self::{datasets::DATASETS, writer::generate_dataset};

pub const DEFAULT_BENCHMARK_CORPUS_RELATIVE: &str =
    "benchmarks/generated_corpus";

#[must_use]
pub fn default_benchmark_corpus_path() -> PathBuf {
    workspace_root().join(DEFAULT_BENCHMARK_CORPUS_RELATIVE)
}

/// # Errors
///
/// Returns an error when the output directory cannot be prepared or files cannot be written.
pub fn generate_benchmark_corpus(
    output_root: &Path,
    clean: bool,
) -> Result<()> {
    if clean && output_root.exists() {
        fs::remove_dir_all(output_root).with_context(|| {
            format!("failed to remove `{}`", output_root.display())
        })?;
    }

    fs::create_dir_all(output_root).with_context(|| {
        format!("failed to create `{}`", output_root.display())
    })?;

    for dataset in DATASETS {
        generate_dataset(output_root, dataset)?;
    }

    fs::write(
        output_root.join("README.txt"),
        "Generated by `sephera_tools generate-benchmark-corpus`.\n",
    )
    .with_context(|| format!("failed to write `{}`", output_root.display()))?;

    Ok(())
}
